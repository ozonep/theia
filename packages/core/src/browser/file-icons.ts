export function getClassWithColor(name) {
	let arr = name.split('.');
	let result;
	let className;
	if (arr.length === 3) {
		result = `${arr[1]}.${arr[2]}}`
	} else if (arr.length === 2) {
		result = arr.pop();
	} else {
		result = arr.shift();
	}
	for (const key in fileExtensions) {
		if (result === key) {
			className = fileExtensions[key];
			break;
		}
	}
	return className;
}
